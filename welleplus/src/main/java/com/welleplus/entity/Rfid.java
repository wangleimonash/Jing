package com.welleplus.entity;

import java.util.Date;

/**
 * @author: David
 * @Description: 基站设备信息
 * @Date: Created in 2017/8/22 13:23
 */
public class Rfid {
    /**
     * RFID设备信息id
     */
    private int id;
    /**
     * RFID设备编号
     */
    private String epc;
    /**
     * 添加RFID设备信息时间
     */
    private Date creadDate;
    /**
     * 修改RFID设备信息时间
     */
    private Date updete;
    /**
     * 关联用户id
     */
    private int uid;
    /**
     * 关联企业id
     */
    private int fid;
    /**
     * RFID设备信息预留字段
     */
    private String type;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEpc() {
        return epc;
    }

    public void setEpc(String epc) {
        this.epc = epc;
    }

    public Date getCreadDate() {
        return creadDate;
    }

    public void setCreadDate(Date creadDate) {
        this.creadDate = creadDate;
    }

    public Date getUpdete() {
        return updete;
    }

    public void setUpdete(Date updete) {
        this.updete = updete;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getFid() {
        return fid;
    }

    public void setFid(int fid) {
        this.fid = fid;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @java.lang.Override
    public java.lang.String toString() {
        return "Rfid1{" +
                "id=" + id +
                ", epc='" + epc + '\'' +
                ", creadDate=" + creadDate +
                ", updete=" + updete +
                ", uid=" + uid +
                ", fid=" + fid +
                ", type='" + type + '\'' +
                '}';
    }
}
